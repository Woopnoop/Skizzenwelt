def parse_level(level_index):
    """Parst ein Level und gibt alle Objekte zurück."""
    if level_index < 0 or level_index >= len(LEVELS):
        return None

    level_data = LEVELS[level_index]
    tilemap = level_data['map']

    platforms = []
    enemies = []
    spikes = []
    moving_platforms = []
    falling_platforms = []
    hearts = []
    flag = None
    player_start = (100, 400)

    rows = len(tilemap)
    cols = max(len(row) for row in tilemap)
    level_width = cols * TILE_SIZE
    level_height = rows * TILE_SIZE

    for row_idx, row in enumerate(tilemap):
        for col_idx, tile in enumerate(row):
            x = col_idx * TILE_SIZE
            y = row_idx * TILE_SIZE

            if tile == '#':
                platforms.append(Platform(x, y))
            elif tile == 'B':
                platforms.append(Box(x, y))
            elif tile == 'S':
                spikes.append(Spike(x, y + TILE_SIZE // 2, TILE_SIZE, TILE_SIZE // 2))
            elif tile == 'P':
                player_start = (x, y - TILE_SIZE)
            elif tile == 'F':
                flag = Flag(x, y - 20)
            elif tile == 'W':
                enemies.append(Walker(x, y - 30))
            elif tile == 'J':
                enemies.append(Jumper(x, y - 30))
            elif tile == 'Y':
                enemies.append(Flyer(x, y))
            elif tile == 'M':
                moving_platforms.append(
                    MovingPlatform(x, y, TILE_SIZE * 3, TILE_SIZE // 2, move_x=1, distance=120)
                )
            elif tile == 'V':
                moving_platforms.append(
                    MovingPlatform(x, y, TILE_SIZE * 2, TILE_SIZE // 2, move_y=1, distance=100)
                )
            elif tile == 'D':
                falling_platforms.append(FallingPlatform(x, y, TILE_SIZE * 2, TILE_SIZE // 2))
            elif tile == 'H':
                hearts.append(HeartPickup(x, y))

    # Kamera erstellen
    camera = Camera(max(level_width, SCREEN_WIDTH), max(level_height, SCREEN_HEIGHT))

    return {
        'name': level_data['name'],
        'platforms': platforms,
        'enemies': enemies,
        'spikes': spikes,
        'moving_platforms': moving_platforms,
        'falling_platforms': falling_platforms,
        'hearts': hearts,
        'flag': flag,
        'player_start': player_start,
        'camera': camera,
        'width': level_width,
        'height': level_height,
    }


class LevelManager:
    """Verwaltet Level-Fortschritt."""
    def __init__(self):
        self.current_level = 0
        self.unlocked = 1  # Anfangs nur Level 1 freigeschaltet
        self.total_levels = len(LEVELS)

    def get_level_name(self, index):
        if 0 <= index < self.total_levels:
            return LEVELS[index]['name']
        return ""

    def load_level(self, index):
        """Lädt ein Level."""
        if index < 0 or index >= self.total_levels:
            return None
        self.current_level = index
        return parse_level(index)

    def complete_level(self):
        """Markiert aktuelles Level als abgeschlossen."""
        if self.current_level + 1 >= self.unlocked:
            self.unlocked = min(self.current_level + 2, self.total_levels)

    def next_level(self):
        """Geht zum nächsten Level."""
        if self.current_level + 1 < self.total_levels:
            self.current_level += 1
            return self.load_level(self.current_level)
        return None

    def is_unlocked(self, index):
        return index < self.unlocked
